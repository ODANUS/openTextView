main() {
  Messages().keys.keys.forEach((k) {
    var appname = Messages().keys[k]!["app_name"];
    var tmpK = k;
    if (tmpK == "zh-CN") {
      tmpK = "zh";
    }
    if (tmpK == "zh-TW") {
      tmpK = "zh-rTW";
    }
    if (tmpK == "zh-HK") {
      tmpK = "zh-rHK";
    }
    Directory d = Directory("../android/app/src/main/res/values-${tmpK}");
    if (!d.existsSync()) {
      d.createSync(recursive: true);
    }
    File f = File("${d.path}/strings.xml");
    if (f.existsSync()) {
      f.create();
    }
    f.writeAsStringSync("""<?xml version="1.0" encoding="utf-8"?>
<resources>
    <string name="app_name">${appname}</string>
</resources>""");
  });
}

